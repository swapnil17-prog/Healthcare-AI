from pydantic import BaseModel, Field
from typing import List, Dict, Any, Optional
from datetime import datetime

class PredictionRequest(BaseModel):
    pregnancies: int = Field(..., ge=0, le=25, description="Number of times pregnant")
    glucose: float = Field(..., ge=0, le=300, description="Plasma glucose concentration")
    blood_pressure: float = Field(..., ge=0, le=200, description="Diastolic blood pressure (mm Hg)")
    insulin: float = Field(..., ge=0, le=1000, description="2-Hour serum insulin (mu U/ml)")
    bmi: float = Field(..., ge=0, le=100, description="Body mass index (weight in kg/(height in m)^2)")
    age: int = Field(..., ge=0, le=120, description="Age in years")

class PredictionOut(BaseModel):
    id: int
    public_id: str
    patient_id: int
    model_name: str
    input_features: Dict[str, Any]
    feature_contributions: Optional[Dict[str, float]] = None
    risk_score: float
    prediction: str
    created_at: datetime
    recommendations: List[str]  # Generated by the doctor recommendation rule engine

    class Config:
        from_attributes = True
